package demo.remindersdemo;

public class Alert {

    private String id;
    private String Name;

    public Alert(){}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
